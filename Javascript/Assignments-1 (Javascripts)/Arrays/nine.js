// How to Access 2D Array item in Javascript.

// Creating 2D Array in Javascript.

let arr = [
    [12, 32, 62, 52, 78],
    [45, 98, 71, 36, 56],
    [10, 1, 34, 60, 3],
];

// Accessing 2D array items using its index value.

console.log(arr[0][2]); // 62
console.log(arr[2][3]); // 60
console.log(arr[2][1]); // 1
console.log(arr[2][4]); // 3
console.log(arr[1][3]); // 36
console.log(arr[1][1]); // 98